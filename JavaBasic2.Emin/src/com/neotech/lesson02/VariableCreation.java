package com.neotech.lesson02;

public class VariableCreation {

	public static void main(String[] args) {
		int number1 = 5;
		int number2;
		number2 = 10;
		char c1;
		char c2;
		char c3;
		// or char c1, c2, c3;
		c1 = 'a';
		c2 = 'b';
		c3 = 'c';

		System.out.println(number1);

		number1 = 10;
		System.out.println(number1);
	}

}
